var trackGrid = [];
const TRACK_ROAD = 0;
const TRACK_WALL = 1;
const TRACK_PLAYERSTART = 2;
const TRACK_GOAL = 3;
const TRACK_TREE = 4;
const TRACK_FLAG = 5;
const TRACK_INVISIBLE_ROAD = 6;

var raceStatus = false;

function returnTileTypeAtColRow(col, row) {
	if(col >= 0 && col < TRACK_COLS && row >= 0 && row < TRACK_ROWS) 
	{
		 var trackIndexUnderCoord = rowColToArrayIndex(col, row);
		 return trackGrid[trackIndexUnderCoord];
	} 
	else 
	{
		return TRACK_WALL;
	}
}

function carTrackHandling(whichCar) {
	var carTrackCol = Math.floor(whichCar.x / TRACK_W);
	var carTrackRow = Math.floor(whichCar.y / TRACK_H);
	var trackIndexUnderCar = rowColToArrayIndex(carTrackCol, carTrackRow);
	
	var previousX = whichCar.x - whichCar.speed;
	var previousY = whichCar.y - whichCar.speed;
	var prevTrackCol = Math.floor(previousX / TRACK_W);
	var prevTrackRow = Math.floor(previousY / TRACK_H);
	
	if(carTrackCol >= 0 && carTrackCol < TRACK_COLS &&
		carTrackRow >= 0 && carTrackRow < TRACK_ROWS) {
	
		var prevTileHere = returnTileTypeAtColRow(prevTrackCol, prevTrackRow);
		var tileHere = returnTileTypeAtColRow(carTrackCol, carTrackRow);
		if(tileHere == TRACK_GOAL) 
		{	
			whichCar.raceStatus = true;
		}
		else if (tileHere == TRACK_ROAD)
		{
			whichCar.raceStatus = false;
		}
		else if (tileHere == TRACK_WALL)
		{		
			whichCar.x -= Math.cos(whichCar.ang) * whichCar.speed;
			whichCar.y -= Math.sin(whichCar.ang) * whichCar.speed;
			whichCar.speed *= -0.5;
		}
		else if (tileHere == TRACK_TREE)
		{		
			whichCar.x -= Math.cos(whichCar.ang) * whichCar.speed;
			whichCar.y -= Math.sin(whichCar.ang) * whichCar.speed;
			whichCar.speed *= -0.5;
		}
		else if (tileHere == TRACK_FLAG)
		{		
			whichCar.x -= Math.cos(whichCar.ang) * whichCar.speed;
			whichCar.y -= Math.sin(whichCar.ang) * whichCar.speed;
			whichCar.speed *= -0.5;
		}
		else if (tileHere == TRACK_INVISIBLE_ROAD)
		{
			if (whichCar.raceStatus == false && whichCar.speed <= 0)
			{
				whichCar.x -= Math.cos(whichCar.ang) * whichCar.speed;
				whichCar.y -= Math.sin(whichCar.ang) * whichCar.speed;
				whichCar.speed *= 1.0;
			}
			if (whichCar.raceStatus == false && whichCar.x - whichCar.speed < whichCar.x)
			{
				whichCar.x -= Math.cos(whichCar.ang) * whichCar.speed;
				whichCar.y -= Math.sin(whichCar.ang) * whichCar.speed;
				whichCar.speed *= -0.5;
			}		
		}
		if (whichCar.raceStatus == true)
		{
			if(prevTileHere == TRACK_ROAD)
			{
				whichCar.lap++;
				if (whichCar.lap == 4)
				{
					whichCar.x = 420;
					whichCar.y = 400;
				}
				if (whichCar.lap == 7)
				{
					player1Car.lap = 0;
					player2Car.lap = 0;
					whichCar.speed = 0;
					loadLevel(levelOne);
					alert(whichCar.name + " wins!");
				}
			}
		}
		//console.log(whichCar.lap);
	} // end of valid col and row
} // end of carTrackHandling func

function rowColToArrayIndex(col, row) {
	return col + TRACK_COLS * row;
}

function drawTracks() {

	var arrayIndex = 0;
	var drawTileX = 0;
	var drawTileY = 0;
	for(var eachRow=0;eachRow<TRACK_ROWS;eachRow++) {
		for(var eachCol=0;eachCol<TRACK_COLS;eachCol++) {
			
			var tileTypeHere = trackGrid[arrayIndex];
			var useImg = trackPics[tileTypeHere];
			
			canvasContext.drawImage(useImg,drawTileX,drawTileY);
			
			drawTileX+= TRACK_W;
			arrayIndex ++;
		} // end of for each col
		drawTileX = 0;
		drawTileY += TRACK_H;
	} // end of for each row

} // end of drawTracks func